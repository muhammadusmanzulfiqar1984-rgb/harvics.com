const fs = require('fs').promises;
const path = require('path');

async function syncLeafImageMap() {
  const verticalsDir = path.join(process.cwd(), 'public', 'assets', 'verticals');
  const outputFile = path.join(process.cwd(), 'src', 'data', 'leafImageMap.ts');
  const imageMap = {};

  try {
    // 1. Scan categories (e.g., fashion)
    const categories = await fs.readdir(verticalsDir);
    
    for (const category of categories) {
      const categoryPath = path.join(verticalsDir, category);
      const stat = await fs.stat(categoryPath);
      if (!stat.isDirectory()) continue;

      // 2. Scan item slugs (e.g., denim-pants)
      const slugs = await fs.readdir(categoryPath);
      for (const slug of slugs) {
        const slugPath = path.join(categoryPath, slug);
        const slugStat = await fs.stat(slugPath);
        if (!slugStat.isDirectory()) continue;

        // 3. Collect all images for the slug
        const files = await fs.readdir(slugPath);
        const imageFiles = files.filter(file => /\.(jpg|jpeg|png|webp)$/i.test(file));
        
        if (imageFiles.length > 0) {
          // Map the slug to an array of its generated local assets
          imageMap[slug] = imageFiles.map(file => `/assets/verticals/${category}/${slug}/${file}`);
        }
      }
    }

    // 4. Atomic write back into the codebase template
    const fileContent = `/**
 * Automatically generated by scripts/sync-leaf-images.js
 * Do not modify manually. Core structural asset mapping.
 */
export const leafImageMap: Record<string, string[]> = ${JSON.stringify(imageMap, null, 2)};
`;

    await fs.writeFile(outputFile, fileContent, 'utf-8');
    console.log(`\x1b[32m[Success]\x1b[0m Successfully synchronized leafImageMap.ts with ${Object.keys(imageMap).length} active catalog items.`);

  } catch (error) {
    console.error(`\x1b[31m[Sync Error]\x1b[0m Failed parsing asset mapping targets:`, error.message);
    process.exit(1);
  }
}

syncLeafImageMap();